colours = ["Red", "Blue", "Yellow", "Green", "Orange", "Purple"]
sentence = "The quick brown fox jumped over the lazy dog."

###slicing

sliced_colours = colours[1:4]
#gives you ["Blue", "Yellow", "Green"]
print(sliced_colours)

sentence_sliced = sentence[4:9]
#gives you "quick"
print(sentence_sliced)

sentence_sliced2 = sentence[4:]
#gives you "quick brown fox jumped over the lazy dog."
print(sentence_sliced2)






